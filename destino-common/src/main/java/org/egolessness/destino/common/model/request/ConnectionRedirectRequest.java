package org.egolessness.destino.common.model.request;

import java.io.Serializable;

/**
 * request of connection redirect
 *
 * @author zsmjwk@outlook.com (wangkang)
 */
public class ConnectionRedirectRequest implements Serializable {

    private static final long serialVersionUID = -7284559966435500860L;

    private String ip;

    private int port;

    private String path;

    public ConnectionRedirectRequest(String ip, int port, String path) {
        this.ip = ip;
        this.port = port;
        this.path = path;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
